// Create modal HTML structure
function createModalStructure() {
  // Create modal overlays for each policy
  const modals = [
    { id: 'privacy-policy-modal', template: 'privacy-policy-template' },
    { id: 'terms-of-service-modal', template: 'terms-of-service-template' },
    { id: 'cookie-policy-modal', template: 'cookie-policy-template' }
  ];
  
  modals.forEach(modal => {
    const modalOverlay = document.createElement('div');
    modalOverlay.id = modal.id;
    modalOverlay.className = 'modal-overlay';
    modalOverlay.setAttribute('aria-hidden', 'true');
    
    modalOverlay.innerHTML = `
      <div class="modal" role="dialog" aria-modal="true">
        <button class="modal-close" aria-label="{{t "modals.closeButton"}}">
          <i class="fas fa-times"></i>
        </button>
        <div data-template="${modal.template}"></div>
      </div>
    `;
    
    document.body.appendChild(modalOverlay);
  });
}

// Initialize modal functionality
document.addEventListener('DOMContentLoaded', function() {
  createModalTemplates();
  createModalStructure();
});